# Privacy Policy What Dinner?

## Data storage
The meals and ingredients you put in What Dinner? are saved in the local storage of your device. This means that no one else has access to your data. It also means your meals cannot be restored when you switch phones or when you accidentally delete your meals.

## Access
What Dinner? has no access to your contacts, your photos or anything else on your device.

## Personal Data
What Dinner? doesn't ask personal data of you. You do not have to create an account to use the app, you remain anonymous when you use the app.

